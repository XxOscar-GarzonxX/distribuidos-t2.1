package co.edu.uptc.distri1.model;

public class Person {
    private String name;
    private String lastname;

    public String getLastname() {
        return lastname;
    }

    public String getName() {
        return name;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public void setName(String name) {
        this.name = name;
    }

}
